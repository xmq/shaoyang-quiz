def render_course(name: str, groups: dict[str, list[str]]) -> str:
    marker = {"高频考点": "🔴", "中频考点": "🔵", "低频了解": "⚫"}
    lines = [f"# {name}：三色笔记", ""]
    lines.append("三色按考频和出题稳定性划分：红色优先背熟，蓝色用于补齐得分点，黑色用于防漏和边界判断。")
    lines.append("")
    for group, items in groups.items():
        lines.append(f"## {marker[group]} {group}")
        lines.append("")
        for item in items:
            lines.append(f"- {marker[group]} {item}")
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def main() -> None:
    COLOR_DIR.mkdir(exist_ok=True)
    for course, groups in COURSES.items():
        (COLOR_DIR / f"{course}.md").write_text(render_course(course, groups), encoding="utf-8", newline="\n")
    print(f"rebuilt {len(COURSES)} color-note files")


if __name__ == "__main__":
    main()
