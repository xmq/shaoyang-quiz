def render_question_line(index: int, question: dict) -> str:
    line = f"{index}. {question['stem']}"
    options = question.get("options") or {}
    if options and question["type"] != "判断":
        line += "  " + "；".join(f"{key}.{value}" for key, value in options.items())
    return line


