def remove_note_supplements():
    """Keep formal drills in the exercise module instead of the lecture source."""
    for course in COURSES:
        path = ROOT / "course_notes" / course["file"]
        text = path.read_text(encoding="utf-8").rstrip()
        if START in text:
            before, rest = text.split(START, 1)
            if END not in rest:
                raise ValueError(f"讲义补充标记不完整：{path.name}")
            _, after = rest.split(END, 1)
            text = (before.rstrip() + "\n" + after.lstrip()).rstrip()
        path.write_text(text + "\n", encoding="utf-8", newline="\n")


