    answers = []
    for course in COURSES:
        bank = grouped[course["subject"]]
        # 四门24题课程避开讲义中最先出现的一组题，形成相对独立的小卷。
        selected = bank[8:16] if len(bank) >= 16 else bank
        papers.extend([
            f"## {course['name']} · 模拟小卷",
            "",
            "建议用时：20分钟。先闭卷作答；计算题必须在草稿纸写出公式、代入、单位和结论。",
            "",
            *[render_question_line(i, q) for i, q in enumerate(selected, 1)],
            "",
        ])
        answers.extend([
            f"## {course['name']} · 答案与评分点",
            "",
            *[
                f"{i}. **{q['answer']}**　{q['explanation']}"
                for i, q in enumerate(selected, 1)
            ],
            "",
        ])
    return "\n".join([
        "# 十五科大学期末模拟小卷",
        "",
        "本册每门课程8题，共15套小卷。题目依据公开高校考核范围重新命制；前半部分只放题目，答案与评分点统一置于后半部分。",
        "",
        "建议做法：每套限时20分钟；正确且能口述理由计1题掌握，只猜对选项不计掌握。做完后把错误归入“概念不清、公式不会、条件漏看、计算失误”四类。",
        "",
        "# 第一部分：闭卷试题",
        "",
        *papers,
        "# 第二部分：答案与教师讲评",
        "",
        *answers,
    ])


